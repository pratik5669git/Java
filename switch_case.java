public class switch_case{
    public static void main(String[] args) {
        int num=3;
        switch (num) {
            case 1:
                System.out.println("hi");
                break;
            case 3:
                System.out.println("hello");
                break;
            default:
                System.out.println("wrong choice");
                break;
        }
    }
}
